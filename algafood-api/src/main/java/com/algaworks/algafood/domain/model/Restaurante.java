package com.algaworks.algafood.domain.model;

import java.math.BigDecimal;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

@Entity
public class Restaurante {
	@Id /* Nessa tabela esse elemento vai ser o ID */
	@GeneratedValue(strategy = GenerationType.IDENTITY) // *esse c√≥digo diz que o ID tem que ser Auto Increment*//
	private Long id;

	@Column(length = 30) /* Coluna na tabela com tamanho de 30 varchar */
	private String nome;

	@Column(name = "taxa_frete") /* Coluna na tabela */
	private BigDecimal taxaFrete; /* Coluna na tabela do tipo BigDecimal */

	// *Getters and setters and Equals and
	// Hashcodes---------------------------------------------------------------------------------------------------------*//
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public BigDecimal getTaxaFrete() {
		return taxaFrete;
	}

	public void setTaxaFrete(BigDecimal taxaFrete) {
		this.taxaFrete = taxaFrete;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Restaurante other = (Restaurante) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}
